#include "uart1.h"
//#include "led.h"
#include "uart2.h"

#define USART1_REC_MAXLEN  			200  	//定义最大接收字节数 200

u8  USART1_RX_BUF[USART1_REC_MAXLEN]; //接收缓冲,最大USART_REC_LEN个字节.末字节为换行符 
u16 USART1_RX_LEN=0;         		//接收状态标记	

//extern u8  UART5_RX_BUF[];

void uart1_init(u32 bound){
   //GPIO端口设置
  GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	
	//U1_RX: PA10   U1_TX: PA9
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA,ENABLE); //使能GPIOA时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1,ENABLE);//使能USART1时钟
 
	//串口1对应引脚复用映射
	GPIO_PinAFConfig(GPIOA,GPIO_PinSource9,GPIO_AF_USART1); //GPIOA9复用为USART1
	GPIO_PinAFConfig(GPIOA,GPIO_PinSource10,GPIO_AF_USART1); //GPIOA10复用为USART1
	
	//USART1端口配置
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9 | GPIO_Pin_10; //GPIOA9与GPIOA10
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF;//复用功能
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;	//速度50MHz
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP; //推挽复用输出
	GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP; //上拉
	GPIO_Init(GPIOA,&GPIO_InitStructure); //初始化PA9，PA10

   //USART1 初始化设置
	USART_InitStructure.USART_BaudRate = bound;//波特率设置
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式
  USART_Init(USART1, &USART_InitStructure); //初始化串口1
	
	
  NVIC_InitStructure.NVIC_IRQChannel = USART1_IRQn;
  NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=1;
  NVIC_InitStructure.NVIC_IRQChannelSubPriority =2;
  NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
  NVIC_Init(&NVIC_InitStructure);
		
  USART_Cmd(USART1, ENABLE);  //使能串口1 
	USART_ClearFlag(USART1,USART_FLAG_TC);
	USART_ITConfig(USART1, USART_IT_RXNE, ENABLE);	

}

void uart1_sendbyte(u8 dat)
{
	USART_SendData(USART1,dat);
  while(USART_GetFlagStatus(USART1, USART_FLAG_TC)!= SET);
  USART_ClearFlag(USART1, USART_FLAG_TC);  
}


void uart1_sendstr(u8* str)
{
	while(*str != '\0')
	{
		uart1_sendbyte(*str);
		str++;
	} 
}

void USART1_IRQHandler(void) 
{
	u8 Res;
	
	
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)
	{
		Res = USART_ReceiveData(USART1);
		USART1_RX_BUF[USART1_RX_LEN] = Res;
		USART1_RX_LEN++;
		if(USART1_RX_LEN>(USART1_REC_MAXLEN-1)) 
			 USART1_RX_LEN=0;
		
		uart2_sendbyte(Res);
	}		
}



